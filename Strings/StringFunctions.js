let txt="Shrinivas ";
// char @ 0th location
console.log(`Char at '0' th location: `,txt.charAt(0));
// 1st char occ @ given index
console.log(`1st occ of 'i' in txt: `,txt.indexOf("i"));
// last char occ @ given index
console.log(`last occ of 'i' in txt: `,txt.lastIndexOf("i"));
// length of string
console.log(`Length of txt: `,txt.length);
// txt to uppercase
console.log(`Uppercase txt: `,txt.toUpperCase());
// txt to lowercase
console.log(`Uppercase txt: `,txt.toLowerCase());
// repeat string
console.log(`txt repeat for 3 times`,txt.repeat(3));
// string slicing
console.log(`1st 4 letters are: `,txt.slice(0,4));