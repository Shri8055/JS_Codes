let student=30;
student+=1; // addition
console.log(`Addition is: ${student}`);
student-=1; // substraction
console.log(`Substraction is: ${student}`);
student*=2; // multiplication
console.log(`Multiplication is: ${student}`);
student/=10; // division
console.log(`Students becomes 60 here!`);
console.log(`Division is: ${student}`);
student**=2; // square
console.log(`Square of students is: ${student}`);